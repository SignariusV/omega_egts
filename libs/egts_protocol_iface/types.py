"""Базовые типы и константы протокола EGTS (независимые от версии ГОСТ)

Модуль содержит:
- Перечисления (PacketType, ServiceType, SubrecordType, RecordStatus, ResultCode)
- Константы протокола (таймауты, CRC полиномы, размеры)

Эти типы используются ядром и не зависят от конкретной реализации.
"""

import enum

# =============================================================================
# Перечисления
# =============================================================================


class PacketType(enum.IntEnum):
    """Типы пакетов транспортного уровня"""

    RESPONSE = 0
    APPDATA = 1
    SIGNED_APPDATA = 2


class ServiceType(enum.IntEnum):
    """Типы сервисов уровня поддержки услуг

    Соответствуют номерам сервисов по ГОСТ 33465.
    """

    AUTH = 1
    COMMANDS = 2
    TRACK = 3
    ACCEL = 4
    ECALL = 5
    FIRMWARE = 6
    TELEMETRY = 7
    TEST_MODE = 8


class SubrecordType(enum.IntEnum):
    """Типы подзаписей (основные, по ГОСТ 33465)

    Значения могут отличаться в разных версиях ГОСТ.
    """

    # Авторизация
    TERM_IDENTITY = 1
    AUTH_PARAMS = 2
    AUTH_INFO = 3
    RESULT_CODE = 4

    # Подтверждения
    RECORD_RESPONSE = 0x8000

    # Телеметрия
    NAV_DATA = 10
    TRACK_DATA = 11
    ACCEL_DATA = 12
    RAW_MSD_DATA = 13

    # Обновление ПО
    SERVICE_PART_DATA = 20
    SERVICE_FULL_DATA = 21

    # Команды
    COMMAND_DATA = 30

    # Режим тестирования
    TEST_MODE_ON = 40
    TEST_MODE_OFF = 41


class RecordStatus(enum.IntEnum):
    """Коды результата обработки записи (RST)"""

    OK = 0
    ERROR = 1
    UNKNOWN_SERVICE = 2
    INVALID_RECORD = 3


class ResultCode(enum.IntEnum):
    """Коды результата обработки (ГОСТ 33465, Приложение В)"""

    OK = 0
    IN_PROGRESS = 1
    UNSUPPORTED_PROTOCOL = 128
    DECRYPT_ERROR = 129
    PROCESS_DENIED = 130
    INCORRECT_FORMAT = 132
    INSUFFICIENT_PARAMS = 134
    HEADER_CRC_ERROR = 137
    DATA_CRC_ERROR = 138
    INVALID_DATA_LEN = 139
    AUTH_DENIED = 151
    ID_NOT_FOUND = 153


# =============================================================================
# Константы протокола
# =============================================================================

# -----------------------------------------------------------------------------
# Таймауты транспортного уровня (ГОСТ 33465, таблица 13)
# -----------------------------------------------------------------------------

TL_RESPONSE_TO = 5
"""Время ожидания подтверждения пакета, секунд"""

TL_RESEND_ATTEMPTS = 3
"""Число повторных попыток отправки"""

TL_RECONNECT_TO = 30
"""Время до повторной попытки установления соединения, секунд"""

# -----------------------------------------------------------------------------
# Таймауты уровня поддержки услуг (ГОСТ 33465, таблица 39)
# -----------------------------------------------------------------------------

EGTS_SL_NOT_AUTH_TO = 6
"""Время ожидания авторизации, секунд"""

# -----------------------------------------------------------------------------
# CRC полиномы (Приложение Д ГОСТ 33465)
# -----------------------------------------------------------------------------

CRC8_POLY = 0x131
"""Полином CRC-8 (с учётом старшего бита)"""

CRC8_INIT = 0xFF
"""Начальное значение CRC-8"""

CRC16_POLY = 0x11021
"""Полином CRC-16 CCITT (с учётом старшего бита)"""

CRC16_INIT = 0xFFFF
"""Начальное значение CRC-16"""

# -----------------------------------------------------------------------------
# Коды ошибок протокола (ГОСТ 33465, Приложение В)
# -----------------------------------------------------------------------------

EGTS_PC_HEADERCRC_ERROR = 137
"""Ошибка контрольной суммы заголовка (CRC-8)"""

EGTS_PC_DATACRC_ERROR = 138
"""Ошибка контрольной суммы данных (CRC-16)"""

# -----------------------------------------------------------------------------
# Минимальные размеры (для валидации)
# -----------------------------------------------------------------------------

PACKET_HEADER_MIN_SIZE = 11
"""Минимальный размер заголовка пакета (без опциональных полей), байт"""

MIN_PACKET_SIZE = 11
"""Минимальный размер пакета (заголовок без данных), байт"""

MIN_RECORD_SIZE = 7
"""Минимальный размер записи (без данных подзаписей), байт"""

MIN_SUBRECORD_SIZE = 3
"""Минимальный размер подзаписи (SRT + SRL), байт"""

# -----------------------------------------------------------------------------
# Максимальные размеры (для валидации)
# -----------------------------------------------------------------------------

MAX_PACKET_SIZE = 4096
"""Максимальный разумный размер пакета EGTS, байт"""

MAX_RECORD_SIZE = 4096
"""Максимальный разумный размер записи, байт"""

MAX_SUBRECORD_SIZE = 4096
"""Максимальный разумный размер подзаписи, байт"""
